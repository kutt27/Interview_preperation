public class TypeCasting {
    public static void main(String[] args) {
        // Casting higher data to lower data
        float a = 25.0000F;
        int b = (int) a;
        System.out.println(b);

        // ascii value to integer
        char ch = 'b';
        int x = ch;
        System.out.println(x);
    }
}
